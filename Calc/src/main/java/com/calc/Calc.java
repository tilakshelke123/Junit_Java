package com.calc;

public class Calc {

	//addition 
	public int add(int a , int b ) {
	
		return a+b;
	}
	
	//subtraction 
	public int sub(int a , int b ) {
		
		return a-b;
	}
	
	//multiplication 
	public int mul(int a , int b ) {
		
		return a*b;
	}
	
	//division 
	
	public int div(int a , int b ) {
		
		return a/b;
	}

	
}
